CREATE TABLE ADDRESS
  (
    ID VARCHAR2(10) not null,
	FLATNO          VARCHAR2(20),
    STREETNO VARCHAR2(20),
    ADDRESSLINE1      VARCHAR2(200),
	ADDRESSLINE2         VARCHAR2(200),
	LANDMARK VARCHAR2(100),
	CITY VARCHAR2(20),
	STATE VARCHAR2(50),
	PINCODE VARCHAR2(20),
	TYPE VARCHAR2(20),
    MEMBERID         VARCHAR2(15),
    GUESTID   VARCHAR2(15),
    WHENCREATED DATE,
	CREATEDBY VARCHAR2(15),
    WHENUPDATED DATE,
    UPDATEDBY VARCHAR2(15),
    TENANTID NUMBER(5,0),
    VERSION NUMBER(5,0) default 1 not null,
    PRIMARY KEY (ID),
    CONSTRAINT FK_ADR_MEMBER FOREIGN KEY (MEMBERID) REFERENCES MEMBER (ID) ENABLE,
    CONSTRAINT FK_ADR_GUEST FOREIGN KEY (GUESTID) REFERENCES GUEST (ID) ENABLE
  );

CREATE TABLE MEMBERROLE
