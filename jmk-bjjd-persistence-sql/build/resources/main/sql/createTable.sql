CREATE TABLE ROLE (
        ID VARCHAR2(10) not null,
        NAME VARCHAR2(70),
        DESCRIPTION VARCHAR2(100),
        WHENCREATED DATE,
		CREATEDBY VARCHAR2(15),
        WHENUPDATED DATE,
        UPDATEDBY VARCHAR2(15),
        TENANTID NUMBER(5,0),
        VERSION NUMBER(5,0) default 1 not null,
        PRIMARY KEY (ID)
);


